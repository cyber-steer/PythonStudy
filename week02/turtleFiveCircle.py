import turtle as t
t.speed(20)
t.pensize(20)
t.shape("turtle")

t.circle(100)

t.up()
t.forward(240)
t.down()
t.pencolor("red")
t.circle(100)

t.up()
t.backward(480)
t.down()
t.pencolor("blue")
t.circle(100)

t.up()
t.right(60)
t.forward(70)
t.down()
t.pencolor("yellow")
t.circle(100)

t.up()
t.left(60)
t.forward(240)
t.right(60)
t.down()
t.pencolor("green")
t.circle(100)

t.done()